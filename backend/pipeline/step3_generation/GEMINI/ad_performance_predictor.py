import pandas as pd
import numpy as np
import lightgbm as lgb
from sklearn.model_selection import GroupKFold
from sklearn.metrics import mean_absolute_error, mean_squared_error

def engineer_features(creatives_df, stats_df):
    # 1. Define efficiency rates (Targets)
    # Adding a small epsilon (1e-5) to avoid division by zero
    eps = 1e-5
    stats_df['CTR'] = stats_df['clicks'] / (stats_df['impressions'] + eps)
    stats_df['CVR'] = stats_df['conversions'] / (stats_df['clicks'] + eps)
    stats_df['VTR'] = stats_df['video_completions'] / (stats_df['impressions'] + eps)
    stats_df['viewability_rate'] = stats_df['viewable_impressions'] / (stats_df['impressions'] + eps)
    
    # 2. Sort data strictly for time-series operations
    stats_df = stats_df.sort_values(by=['creative_id', 'country', 'os', 'days_since_launch']).reset_index(drop=True)
    
    # 3. Create Autoregressive Features (Grouped by entity)
    # We group by creative, country, and OS so lags don't bleed across different ads or segments
    group_cols = ['creative_id', 'country', 'os']
    
    targets = ['CTR', 'CVR', 'VTR', 'viewability_rate', 'spend_usd']
    for col in targets:
        # Lags (1 and 2 days) - These were fine!
        stats_df[f'{col}_lag_1'] = stats_df.groupby(group_cols)[col].shift(1)
        stats_df[f'{col}_lag_2'] = stats_df.groupby(group_cols)[col].shift(2)
        
        # THE FIX: Chain .shift(1) before .rolling()
        stats_df[f'{col}_rolling_mean_3d'] = stats_df.groupby(group_cols)[col].transform(
            lambda x: x.shift(1).rolling(window=3, min_periods=1).mean()
        )
        stats_df[f'{col}_rolling_std_3d'] = stats_df.groupby(group_cols)[col].transform(
            lambda x: x.shift(1).rolling(window=3, min_periods=1).std()
        )
    # Fill NaN values generated by shifting (for the first few days of a campaign)
    # stats_df.fillna(0, inplace=True) TODO
    stats_df['is_cold_start'] = (stats_df['days_since_launch'] <= 3).astype(int)
    
    # 4. Merge with creatives data
    df = pd.merge(stats_df, creatives_df, on='creative_id', how='left')
    
    # 5. Drop forbidden and redundant columns
    cols_to_drop = [
        'advertiser_name', 'app_name', 'campaign_id_x', 'campaign_id_y', 
        'date', 'asset_file', 'impressions_last_7d', 'creative_launch_date',
        # Drop current day absolute metrics to prevent data leakage
        'impressions', 'viewable_impressions', 'clicks', 'conversions', 
        'revenue_usd', 'video_completions', 'spend_usd', 'cta_text', 
        'headline', 'subhead' # ep cta_text, headline, subhead els he tret!
    ]
    
    df = df.drop(columns=[col for col in cols_to_drop if col in df.columns], errors='ignore')
    
    # 6. Convert categoricals for LightGBM
    categorical_cols = [
        'country', 'os', 'vertical', 'format', 'language', 'theme', 
        'hook_type', 'dominant_color', 'emotional_tone'# , 'cta_text', 
        # 'headline', 'subhead'
    ]
    
    for col in categorical_cols:
        if col in df.columns:
            df[col] = df[col].astype('category')
            
    return df

