def train_lgbm_model(df, target_col='CTR'):
    print(f"--- Training Model for Target: {target_col} ---")
    
    # Define features (X) and target (y)
    # We drop all target variables from the feature set so it only learns from lags
    all_targets = ['CTR', 'CVR', 'VTR', 'viewability_rate']
    features = [col for col in df.columns if col not in all_targets and col != 'creative_id']
    
    X = df[features]
    y = df[target_col]
    groups = df['creative_id'] # Used to prevent data leakage across splits
    
    # GroupKFold configuration
    gkf = GroupKFold(n_splits=5)
    
    oof_predictions = np.zeros(len(df))
    models = []
    
    for fold, (train_idx, val_idx) in enumerate(gkf.split(X, y, groups)):
        print(f"Fold {fold + 1}")
        
        X_train, y_train = X.iloc[train_idx], y.iloc[train_idx]
        X_val, y_val = X.iloc[val_idx], y.iloc[val_idx]
        
        # Create LightGBM datasets
        train_data = lgb.Dataset(X_train, label=y_train)
        val_data = lgb.Dataset(X_val, label=y_val, reference=train_data)
        
        # LightGBM Parameters (These are good starting defaults for tabular regressors)
        params = {
            'objective': 'regression',
            'metric': 'rmse',
            'boosting_type': 'gbdt',
            'learning_rate': 0.05,
            'num_leaves': 31,
            'feature_fraction': 0.8,
            'verbose': -1,
            'random_state': 42
        }
        
        # Train model with early stopping
        model = lgb.train(
            params,
            train_data,
            num_boost_round=1000,
            valid_sets=[train_data, val_data],
            callbacks=[lgb.early_stopping(stopping_rounds=50, verbose=False)]
        )
        
        # Store predictions and model
        oof_predictions[val_idx] = model.predict(X_val, num_iteration=model.best_iteration)
        models.append(model)
        
        fold_rmse = np.sqrt(mean_squared_error(y_val, oof_predictions[val_idx]))
        print(f"Fold {fold + 1} RMSE: {fold_rmse:.5f}")
        
    # Overall Validation Metrics
    overall_rmse = np.sqrt(mean_squared_error(y, oof_predictions))
    overall_mae = mean_absolute_error(y, oof_predictions)
    print(f"\nOverall Out-of-Fold RMSE: {overall_rmse:.5f}")
    print(f"Overall Out-of-Fold MAE: {overall_mae:.5f}\n")
    
    return models, features

